from datetime import datetime, timedelta

def get_upcoming_birthdays(users):
    upcoming_birthdays = []
    today = datetime.today().date()

    for user in users:
        birthday = datetime.strptime(user["birthday"], "%Y.%m.%d").date()
        birthday_this_year = birthday.replace(year=today.year)

        if birthday_this_year < today:
            birthday_this_year = birthday_this_year.replace(year=today.year + 1)

        days_to_birthday  = (birthday_this_year - today).days

        if days_to_birthday  <= 7:
            if birthday_this_year.weekday() in [5, 6]: 
                weekday_difference = (7 - birthday_this_year.weekday()) % 7 
                birthday_greeting_date = (birthday_this_year + timedelta(days=weekday_difference)).strftime("%Y.%m.%d")
            else:
                birthday_greeting_date = birthday_this_year.strftime("%Y.%m.%d")
            upcoming_birthdays.append({"name": user["name"], "Happy birthdaysğŸ‚": birthday_greeting_date})

    return upcoming_birthdays


